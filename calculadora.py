class Calculadora:
    def sumar(self, a, b):  
        return a + b
    def restar(self, a, b):
        return a - b
    def multiplicar(self, a, b):
        return a * b
    def dividir(self, a, b):
        return a / b
    def potencia(self, a, b):
        return a ** b
    def modulo(self, a, b):
        return a % b
